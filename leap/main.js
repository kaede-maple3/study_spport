let tableEle = ["連番", "単語", "意味"];
function drawAll() {
    let befp = 0, befw = 0;
    let pele, wele, tele;
    let count = 1;
    for (let word in words) {
        let p = words[word][0];
        let w = words[word][1];

        if (befp != p) {//Part切り替え
            befp = p;
            let details = document.createElement("details");
            let summary = document.createElement("summary");
            summary.textContent = "Part" + p + "(" + wc[p - 1][0] + "語)";
            details.appendChild(summary);
            wordDiv.appendChild(details);
            pele = details;
        }

        if (befw != w) {//Week切り替え
            befw = w;
            let details = document.createElement("details");
            let summary = document.createElement("summary");
            summary.textContent = "Week" + w + "(" + wc[p - 1][1][w - 1][0] + "~" + wc[p - 1][1][w - 1][1] + ")(" + (wc[p - 1][1][w - 1][1] - wc[p - 1][1][w - 1][0] + 1) + "語)";
            details.appendChild(summary);
            pele.appendChild(details);

            let table = document.createElement("table");
            details.appendChild(table);
            let thead = document.createElement("thead");
            table.appendChild(thead);
            let tr = document.createElement("tr");
            thead.appendChild(tr);
            for (let e in tableEle) {
                let th = document.createElement("th");
                th.textContent = tableEle[e];
                tr.appendChild(th);
            }

            wele = details;
            tele = table;
        }

        //単語追加
        let tbody = document.createElement("tbody");
        tele.appendChild(tbody);
        let tr = document.createElement("tr");
        tbody.appendChild(tr);

        let thc = document.createElement("th");
        thc.textContent = count;
        tr.appendChild(thc);

        let thw = document.createElement("th");
        thw.textContent = word;
        tr.appendChild(thw);

        let thm = document.createElement("td");
        thm.textContent = words[word][2];
        console.log(thm.style)
        tr.appendChild(thm);

        count++;
    }
}